package com.kulturman.irembotest.infrastructure.persistence;

import com.kulturman.irembotest.domain.entities.User;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.Pageable;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.stereotype.Repository;

import java.util.Optional;
import java.util.UUID;

@Repository
public interface UserDb extends JpaRepository<User, UUID> {
    Optional<User> findByEmail(String email);
    Page<User> findAllByTenantId(UUID tenantId, Pageable pageable);
}
